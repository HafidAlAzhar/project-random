# If Pengkondisian
print('Masukan jumlah manusia: ')
manusia = int(input())

print('Masukan jumlah zombie')
zombie = int(input())

if manusia < zombie:
    print('Kabur kita akan mati')

if manusia > zombie:
    print("Ayo bunuh zombie yang ada")

if manusia == zombie:
    print('Mari basmi zombie')

