# Operator Aritmatika

# Penjumlahan
apel = 12
jeruk = 24
jumlah = apel + jeruk
print(jumlah, "Buah")

# Pengurangan
hutang = 5000
bayar = 2000
sisaHutang = hutang - bayar
print(sisaHutang, "Ribu")

# Perkalian
panjang = 30
lebar = 15
luas = panjang * lebar
print(luas, "CM")

# Pembagian
kue = 40
anak = 8
kueperanak = kue // anak
print( "Setiap anak mendapatakan kue sebanyak : ",kueperanak)

# Sisa bagi / Modulus
bil1 = 14
bil2 = 5
hasil = bil1 % bil2
print("Sisa bagi dari", bil1, "dan", bil2, "hasilnya", hasil) 

# Pangkat
bil3 = 8
bil4 = 4
hasilPangkat = bil3 ** bil4
print(hasilPangkat) 

# Pembagian Bulat
print(10//3)

